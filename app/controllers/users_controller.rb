class UsersController < ApplicationController
  include MainConcern

  before_action :set_user, only: %i[ show edit update destroy follow unfollow ]
  before_action :set_current_user
  before_action only: %i[ edit destroy ] do
    if @user
      is_the_right_user(@user.id)
    else
      is_the_right_user(-1)
    end
  end


  # GET /users or /users.json
  def index
    @users = User.all
  end

  # GET /users/1 or /users/1.json
  def show
  end

  # GET /users/new
  def new
    @user = User.new
  end

  # GET /users/1/edit
  def edit
  end

  # POST /users or /users.json
  def create
    @user = User.new(user_params)

    respond_to do |format|
      if @user.save
        format.html { redirect_to main_path, notice: "User was successfully created." }
        #format.json { render :show, status: :created, location: @user }
      else
        format.html { render :new, status: :unprocessable_entity }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /users/1 or /users/1.json
  def update
    respond_to do |format|
      if @user.update(user_params)
        format.html { redirect_to @user, notice: "User was successfully updated." }
        format.json { render :show, status: :ok, location: @user }
      else
        format.html { render :edit, status: :unprocessable_entity }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /users/1 or /users/1.json
  def destroy
    @user.destroy
    respond_to do |format|
      format.html { redirect_to users_url, notice: "User was successfully destroyed." }
      format.json { head :no_content }
    end
  end

  def follow
    #puts "--------------------------#{@user}"
    @currentUser.followees << @user
    redirect_back(fallback_location: profile_path(@user))
  end

  def unfollow
    @currentUser.followed_users.find_by(followee_id: @user).destroy
    redirect_back(fallback_location: profile_path(@user))
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_user
      @user = User.find(params[:id])
    end

    # Only allow a list of trusted parameters through.
    def user_params
      params.require(:user).permit(:name, :email, :password, :password_confirmation)
    end
    
end
